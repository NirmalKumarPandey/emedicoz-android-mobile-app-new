package com.emedicoz.app.podcast;

public class Author {
    private String id;
    private String name;
    private String email;
    private String mobile;
    private String gender;
    private String is_add_podcast;
    private String master_id;
    private String image;
    private String podcast_count;
    private String is_new;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getIs_add_podcast() {
        return is_add_podcast;
    }

    public void setIs_add_podcast(String is_add_podcast) {
        this.is_add_podcast = is_add_podcast;
    }

    public String getMaster_id() {
        return master_id;
    }

    public void setMaster_id(String master_id) {
        this.master_id = master_id;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPodcast_count() {
        return podcast_count;
    }

    public void setPodcast_count(String podcast_count) {
        this.podcast_count = podcast_count;
    }

    public String getIs_new() {
        return is_new;
    }

    public void setIs_new(String is_new) {
        this.is_new = is_new;
    }
}
